import { Schema, model } from "mongoose";

const AuthorSchema = new Schema({
  author_id: {
    type: String,
    required: true,
  },
  name: {
    type: String,
    required: true,
  },
  age: {
    type: Number,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: true,
  },
});

let Author = model("Author", AuthorSchema);

export default Author;
